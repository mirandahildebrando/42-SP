#include <unistd.h>

void	ft_ft(int *nbr);

void	ft_ft(int *nbr)
{
	*nbr = 42;
}


int	main(void)
{
	int a;

	ft_ft(&a);

	char c, d;

	c = (a / 10) + '0';
	d = (a % 10) + '0';

	write(1, &c, 1);
	write(1, &d, 1);
	return (0);
} 
